#include <stdio.h>

int main(void)
{
    int x = 3; // Number = int, name = x and datatype = int
    int y = 3;

    if (x < y)  // True/ False
    {
        printf("x is less than y\n");
    }
    else if (x > y)  
    {
        printf("x is greater than y\n");
    }
    else  
    {
        printf("x is equal to y\n"); 
    }
    
}

